Welcome to Samurai Framework.
This Framework is Powerful fullstacked framework.

Usage:
    samurai [option] [command] [command-option] [args...]

Options:
    --version, -[vV]            Print version and exit successfully.
    --help,    -[hH]            Print this help and exit successfully.

Commands:
    add-project                 Add Project.
    add-application             Add Application.
    add-action                  Add Action.
    add-component               Add Component.
    add-filter                  Add Filter.
    add-template                Add Template.
    add-migration               Add Migration files.
    add-spec                    Add Spec files.


